package univ.donga.dw.info.seat;

public class SeatCampusInfo {
	private String name;
	private String totalSeat;
	private String useSeat;
	private String remainSeat;
	private String useRate;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public String getTotalSeat() {
		return totalSeat;
	}
	public void setTotalSeat(String totalSeat) {
		this.totalSeat = totalSeat;
	}
	public String getUseSeat() {
		return useSeat;
	}
	public void setUseSeat(String useSeat) {
		this.useSeat = useSeat;
	}
	public String getRemainSeat() {
		return remainSeat;
	}
	public void setRemainSeat(String remainSeat) {
		this.remainSeat = remainSeat;
	}
	public String getUseRate() {
		return useRate;
	}
	public void setUseRate(String useRate) {
		this.useRate = useRate;
	}
	
}
